local function mkAI(factionId)
    local AI = require("ai")
    local ai = AI.new({factionId = factionId})
    ai.grid = {
        getUnitAt = function()
            return nil
        end
    }
    return ai
end

local function hasFactionAttack(actions, actingPlayer)
    local function targetPlayer(state, row, col)
        for _, unit in ipairs((state and state.units) or {}) do
            if unit and unit.row == row and unit.col == col then
                return tonumber(unit.player) or 0
            end
        end
        for player = 1, 2 do
            local hub = state and state.commandHubs and state.commandHubs[player]
            if hub and hub.row == row and hub.col == col then
                return player
            end
        end
        return 0
    end

    for _, action in ipairs(actions or {}) do
        if action and action.type == "attack" and action.target then
            local player = targetPlayer(_G.__attackFixtureState, action.target.row, action.target.col)
            if player > 0 and player ~= tonumber(actingPlayer) then
                return true
            end
        end
    end

    return false
end

runTest("tournament_prefers_faction_attack_under_draw_pressure_fixture", function()
    ensureHeadlessGlobals()
    GAME.CURRENT.AI_PLAYER_NUMBER = 1

    local fixtureLib = require("scripts.ai_tournament_fixture_lib")
    local fixture = fixtureLib.getFixture("draw_pressure_faction_attack_priority")
    assertTrue(fixture ~= nil, "fixture draw_pressure_faction_attack_priority missing")

    local ai = mkAI(1)
    ai.AI_PARAMS.TOURNAMENT_AI.ENABLED = true
    ai.AI_PARAMS.TOURNAMENT_AI.LOG_SUMMARY = false

    _G.__attackFixtureState = fixture.state
    local sequence = ai:getBestSequence(fixture.state)
    local meta = ai.lastTournamentMeta or {}
    local stats = meta.stats or {}

    assertTrue(type(sequence) == "table" and #sequence > 0, "Tournament should return a sequence")
    assertTrue((tonumber(stats.legalAttackActions) or 0) > 0, "fixture should expose legal faction attack actions")
    assertTrue(stats.selectedHasFactionAttack == true, "Tournament should select faction attack under draw pressure fixture")
    assertTrue(stats.selectedPassiveOnly ~= true, "selected turn must not be passive-only under attack opportunity")
    assertTrue(hasFactionAttack(sequence, 1), "selected sequence should contain faction-vs-faction attack")

    _G.__attackFixtureState = nil
end)

local function buildReport()
    local passCount = 0
    for _, result in ipairs(results) do
        if result.ok then
            passCount = passCount + 1
        end
    end

    local lines = {}
    lines[#lines + 1] = "# Tournament Attack Priority Smoke"
    lines[#lines + 1] = ""
    lines[#lines + 1] = "- Generated: " .. os.date("%Y-%m-%d %H:%M:%S")
    lines[#lines + 1] = "- Passed: " .. tostring(passCount)
    lines[#lines + 1] = "- Failed: " .. tostring(#results - passCount)
    lines[#lines + 1] = ""
    lines[#lines + 1] = "## Results"
    lines[#lines + 1] = ""

    for _, result in ipairs(results) do
        local status = result.ok and "PASS" or "FAIL"
        lines[#lines + 1] = string.format("- `%s` %s (%.2fms)", status, result.name, result.ms)
        if not result.ok then
            lines[#lines + 1] = "  - Error: `" .. tostring(result.err):gsub("\n", " ") .. "`"
        end
    end

    return table.concat(lines, "\n")
end

local report = buildReport()
print(report)

local hasFailure = false
for _, result in ipairs(results) do
    if not result.ok then
        hasFailure = true
        break
    end
end

os.exit(hasFailure and 1 or 0)
