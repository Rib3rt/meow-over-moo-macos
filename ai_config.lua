local function parseBooleanEnv(varName, defaultValue)
    local raw = os.getenv(varName)
    if raw == nil or raw == "" then
        return defaultValue
    end

    local normalized = tostring(raw):lower()
    if normalized == "1" or normalized == "true" or normalized == "yes" or normalized == "on" then
        return true
    end
    if normalized == "0" or normalized == "false" or normalized == "no" or normalized == "off" then
        return false
    end

    return defaultValue
end

local function parseNumberEnv(varName, defaultValue)
    local raw = os.getenv(varName)
    if raw == nil or raw == "" then
        return defaultValue
    end

    local parsed = tonumber(raw)
    if parsed == nil then
        return defaultValue
    end
    return parsed
end

